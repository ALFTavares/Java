
public class Calculadora {

	public Calculadora(){
		
	}
	
	public double soma(double v1, double v2){
		return v1 + v2;
	}
	
	public double subtrai(double v1, double v2){
		return v1 - v2;
	}
	
	public double mult(double v1, double v2){
		return v1 * v2;
	}
	
	public double div(double v1, double v2){
		return v1 / v2;
	}
}
